def seconds_to_hours(s):
    """Convert seconds to hours:

    :param s: Number of seconds
    :type s: Float
    :return: Number of hours
    :rtype: Float
    """
    return float(s) / 3600


def hours_to_days(h):
    """Convert hours to days.

    :param h: Number of hours
    :type h: Float
    :return: Number of days
    :rtype: Float
    """
    return float(h) / 24
